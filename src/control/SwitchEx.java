package control;

public class SwitchEx {
    public static void main(String[] args) {
        int number = 3;

        switch (number) {
            case 3:
                System.out.print("*");
                break;
            case 2:
                System.out.print("*");
                break;
            case 1:
                System.out.print("*");
                break;
        }
    }
}
